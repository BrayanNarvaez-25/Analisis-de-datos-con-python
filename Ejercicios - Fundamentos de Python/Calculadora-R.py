print("Programa para caluclar el área, perímetro y superficie del rectángulo")

base = float(input("\nIngrese la base: "))
altura = float(input("Ingrese la altura: "))

areaYSuperficie = base * altura
perimetro = 2*(base) + 2*(altura)

print(f"\nÁrea: {areaYSuperficie} \nPerímetro: {perimetro} \nSuperficie: {areaYSuperficie} ")
